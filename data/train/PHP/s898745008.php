<?php
$output = array();

while (true) {

  $words = array();
  $times = array();
  $score = array();
  $minMember = array();
  $member = 0;

  // ?????°??????
  $member = trim(fgets(STDIN));

  // ????????????
  if ($member == "0") {
    break;
  }

  // ?????°??????????????°?????????
  for ($j = 0; $j < $member; $j++) {

    // ????????§??????
    $words = preg_split("/[\s]/", trim(fgets(STDIN)));

    // ???????????????
    $name = $words[0];

    // score?????????????????????????????????
    $score[$name] = 0;

    // ?¨??????????????????\?????????????????°????????????
    $kind = $words[1];

    for ($k = 2; $k < count($words); $k++) {
      // ????????£??????????????????
      $times[$words[$k]][] = $name;
    }
  }

  // ?????°?¨????
  foreach ($times as $key => $value) {

    $number = count($value);

    foreach ($value as $character) {
      $score[$character] += $member - $number + 1;
    }
  }

  // ????????¢????°????????????¢???
  $min = min($score);

  foreach ($score as $key => $value) {
    if ($min == $value) {
      $minMember[] = $key;
    }
  }

  // ????????????
  sort($minMember);

  // ??????
  $output[] = $min . ' ' . $minMember[0];
}

