function bomber($data, $x, $y){
	if($x > 0 && $y > 0 && $x < 9 && $y < 9 && $data[$y][$x] > 0){
		// ??????
		$data[$y][$x] = 0;

		// ?????????????????????????????????
		for($i = 1; $i <= 3; $i++){
			$data = bomber($data, $x-$i, $y);
			$data = bomber($data, $x+$i, $y);
			$data = bomber($data, $x, $y-$i);
			$data = bomber($data, $x, $y+$i);
		}

	}

	return $data;
}


