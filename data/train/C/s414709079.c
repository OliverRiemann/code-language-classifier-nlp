void QSort(int x[], int left, int right)
{
	int i, j;
	int pivot;
	
	i = left;
	j = right;

	pivot = x[(left + right) / 2];

	while (1) {
		while (x[i] < pivot) {
			i++;
		}
		while (x[j] > pivot) {
			j--;
		}
		if (i >= j) {
			break;
		}
		Swap(x, i, j);
		i++;
		j--;
	}

	if (left < i - 1)              /* ?????????????????? 2 ??\???????´????????????° */
		QSort(x, left, i - 1);     /* ??????????????? Q ??????????????? */
	if (j + 1 <  right)            /* ?????????????????? 2 ??\???????´????????????° */
		QSort(x, j + 1, right);    /* ??????????????? Q ??????????????? */
}

